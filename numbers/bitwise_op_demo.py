# bitwise operators

print(f"10 = '{10:04b}'")
print(f"4  = '{4:04b}'")
print(f"8  = '{8:04b}'")
print()

# bitwise AND
print("10 & 4 =", 10 & 4)
print("10 & 8 =", 10 & 8)

# bitwise OR
print("10 | 4 =", 10 | 4)
print("10 | 8 =", 10 | 8)

# bitwise XOR
print("10 ^ 4 =", 10 ^ 4)
print("10 ^ 8 =", 10 ^ 8)

# bitwise NOT
print(f"10  = '{10:b}'")
print(f"-11 = '{-11:b}'")
print("~10 =", ~10)
print("~-11 =", ~-11)
print(f"~0  =", ~0)
print("~-1 =", ~-1)

# bitwise right shift (like floor division)
print("5 >> 1 =", 5 >> 1)
print("-5 >> 1 =", -5 >> 1)
print("10 >> 3 =", 10 >> 3)
print("-10 >> 3 =", -10 >> 3)
print("10 >> 4 =", 10 >> 4)
print("-10 >> 4 =", -10 >> 4)

# bitwise left shift
print("5 << 1 =", 5 << 1)
print("-5 << 1 =", -5 << 1)
